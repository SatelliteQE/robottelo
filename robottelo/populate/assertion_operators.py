# coding: utf-8


def eq(value, other):
    return value == other


def ne(value, other):
    return value != other


def gt(value, other):
    return value > other


def lt(value, other):
    return value < other


def gte(value, other):
    return value >= other


def lte(value, other):
    return value <= other


def identity(value, other):
    return value is other